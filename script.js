// FUNCTIONS

// ADD

function add(a,b) {
    return a + b;
}

// SUBTRACT

function subtract(a,b) {
    return a - b;
}

// MULTIPLY

function multiply(a,b) {
    return a * b;
}

// DIVIDE

function divide(a,b) {
    return a / b;
}















// TESTING
console.log(add(2,2));
console.log(subtract(2,2));
console.log(multiply(2,2));
console.log(divide(2,2));